//ダブルバッファのうち適切な方のバッファからCPUインデックスを出す
D3D12_CPU_DESCRIPTOR_HANDLE DepthStencilBuffer::OutProperCPUHandle()const
{
	D3D12_CPU_DESCRIPTOR_HANDLE cpuHandle;

	//１枚目がDSVだった場合はそれのCPUhandleを出す
	if (status == kDSV_SRV)
	{
		cpuHandle = WatchIndex<ViewType::kDSV, D3D12_CPU_DESCRIPTOR_HANDLE>(0);
	}
	else
	{
		cpuHandle = WatchIndex<ViewType::kDSV, D3D12_CPU_DESCRIPTOR_HANDLE>(0);
	}

	return cpuHandle;
}
