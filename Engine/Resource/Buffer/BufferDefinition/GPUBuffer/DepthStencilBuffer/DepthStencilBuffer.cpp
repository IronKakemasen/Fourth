DepthStencilBuffer::DepthStencilBuffer
(
	const InstanceKey& instanceKey_,
	std::string name_,
	std::vector<Microsoft::WRL::ComPtr<ID3D12Resource>> resourceContainer_,
	std::unique_ptr <BufferDescriptionBehavior>&& description_
) : GPUBufferBehavior(instanceKey_, name_, std::move(resourceContainer_), std::move(description_))
{
	//ステータスを同期
	SynchronizeStatus(description->numBuffer);
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
D3D12_RESOURCE_BARRIER DepthStencilBuffer::CreateBarrier(Usage usage_)
{
	int dstBufferIndex = ProperBufferIndex(usage_);

	return buffers.at(dstBufferIndex).CreateBarrier(ResourceStateTable(usage_));
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

D3D12_CPU_DESCRIPTOR_HANDLE DepthStencilBuffer::OutProperDSVHeapHandle()const
{
	int dstBufferIndex = ProperBufferIndex(ViewType::kDSV);

	return WatchIndex<ViewType::kDSV, D3D12_CPU_DESCRIPTOR_HANDLE >(dstBufferIndex);
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
uint32_t DepthStencilBuffer::OutProperSRVHeapIndex()const
{
	int dstBufferIndex = ProperBufferIndex(ViewType::kSRV);

	return WatchIndex<ViewType::kSRV, uint32_t >(dstBufferIndex);
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
DXGI_FORMAT DepthStencilBuffer::OutProperDSVFormat()const
{
	return static_cast<DepthStencilBufferDescription*>(description.get())->WatchParam().dsvFormat;
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
std::array<float, 4> DepthStencilBuffer::OutProperClearColor()const
{
	std::array<float, 4> clearColor = { static_cast<DepthStencilBufferDescription*>(description.get())->WatchParam().clearColor,0,0,0 };

	return clearColor;
}
