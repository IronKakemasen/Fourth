ColorBuffer::ColorBuffer
(
	const InstanceKey& instanceKey_,
	std::string name_,
	std::vector<Microsoft::WRL::ComPtr<ID3D12Resource>> resourceContainer_,
	std::unique_ptr <BufferDescriptionBehavior>&& description_
) : GPUBufferBehavior(instanceKey_, name_, std::move(resourceContainer_), std::move(description_))
{
	//ステータスを同期
	SynchronizeStatus(description->numBuffer);
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
D3D12_RESOURCE_BARRIER ColorBuffer::CreateBarrier(Usage usage_)
{
	int dstBufferIndex = ProperBufferIndex(usage_);

	return buffers.at(dstBufferIndex).CreateBarrier(ResourceStateTable(usage_));
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
D3D12_CPU_DESCRIPTOR_HANDLE ColorBuffer::OutProperRTVHeapHandle()const
{
	int dstBufferIndex = ProperBufferIndex(Usage::kWrite);

	return WatchIndex<ViewType::kRTV, D3D12_CPU_DESCRIPTOR_HANDLE >(dstBufferIndex);
}
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///+///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
uint32_t ColorBuffer::OutProperSRVHeapIndex(int frameIndex_)const
{
	int dstBufferIndex = ProperBufferIndex(Usage::kRead);

	return WatchIndex<ViewType::kSRV, uint32_t >(dstBufferIndex);
}
